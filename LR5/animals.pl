% Факты: животные и их характеристики
% Структура: animal(Имя, Тип_питания, Место_обитания, Хвост, Класс, Размер)
animal(лев, хищник, саванна, да, млекопитающее, крупный).
animal(жираф, травоядное, саванна, да, млекопитающее, крупный).
animal(пингвин, плотоядное, антарктида, да, птица, средний).
animal(дельфин, плотоядное, океан, да, млекопитающее, крупный).
animal(заяц, травоядное, лес, да, млекопитающее, малый).
animal(страус, травоядное, пустыня, да, птица, крупный).
animal(акула, хищник, океан, да, рыба, крупный).
animal(кенгуру, травоядное, австралия, да, млекопитающее, средний).
animal(белка, всеядное, лес, да, млекопитающее, малый).
animal(осьминог, плотоядное, океан, нет, моллюск, средний).

% Правило 1: Травоядные животные саванны
savanna_herbivore(X) :-
    animal(X, травоядное, саванна, _, _, _).

% Правило 2: Животные с хвостом
has_tail(X) :-
    animal(X, _, _, да, _, _).

% Правило 3: Крупные хищники океана
ocean_predator(X) :-
    animal(X, хищник, океан, _, _, крупный).

% Правило 4: Птицы пустынь
desert_bird(X) :-
    animal(X, _, пустыня, _, птица, _).

% Правило 5: Мелкие всеядные млекопитающие
small_omnivore(X) :-
    animal(X, всеядное, _, _, млекопитающее, малый).